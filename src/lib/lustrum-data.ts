// Helper functies
export function getDailyFact(): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24)
  )
  return MALLORCA_FACTS[dayOfYear % MALLORCA_FACTS.length]
}

export function getCountdown(): { days: number; hours: number; minutes: number; seconds: number } {
  const now = new Date()
  const diff = LUSTRUM_CONFIG.startDate.getTime() - now.getTime()

  if (diff <= 0) {
    return { days: 0, hours: 0, minutes: 0, seconds: 0 }
  }

  return {
    days: Math.floor(diff / (1000 * 60 * 60 * 24)),
    hours: Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)),
    minutes: Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60)),
    seconds: Math.floor((diff % (1000 * 60)) / 1000),
  }
}

export function getRandomHotspots(count: number = 4): Hotspot[] {
  const shuffled = [...HOTSPOTS].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getHotspotsByCategory(category: HotspotCategory): Hotspot[] {
  return HOTSPOTS.filter(h => h.category === category)
}

